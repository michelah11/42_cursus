#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*s_ptr;

	s_ptr = (unsigned char *)s;
	while (n-- > 0)
	{
		*s_ptr = '\0';
		s_ptr++;
	}
}
