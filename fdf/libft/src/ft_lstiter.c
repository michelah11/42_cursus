#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*temp;

	if (!lst || !f)
		return ;
	temp = (t_list *)lst;
	while (temp)
	{
		f(temp->content);
		temp = temp->next;
	}
}
